def analyze_for_web(combined_input: str, api_key: str) -> dict:
    client = anthropic.Anthropic(api_key=api_key)

    instruction_re = re.compile(
        r"(해줘|해주세요|반영해|참고해|넣어줘|만들어줘|수정해줘)$|"
        r"^(줄테니|참고로|그리고|혹시|ai가|claude가|너가)",
        re.IGNORECASE
    )
    def clean(text):
        lines = [l for l in text.splitlines() if not instruction_re.search(l.strip())]
        return "\n".join(lines)

    response = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=16000,
        system=[{"type": "text", "text": SYSTEM_PROMPT,
                 "cache_control": {"type": "ephemeral"}}],
        messages=[{"role": "user",
                   "content": PROMPT.format(combined_input=clean(combined_input))}],
    )

    raw = response.content[0].text.strip()
    if raw.startswith("```"):
        raw = raw.split("```")[1]
        if raw.startswith("json"):
            raw = raw[4:]
    return json.loads(raw.strip())
