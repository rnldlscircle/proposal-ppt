def analyze_and_generate_storyline(
    direct_input: str = "",
    slack_data: str = "",
    customer_web_data: str = "",
    industry_news: str = "",
    api_key: str = "",
) -> dict:
    client = anthropic.Anthropic(api_key=api_key)

    # 사용자 지시어 줄 제거 (내용이 아닌 명령문)
    instruction_re = re.compile(
        r"(해줘|해주세요|반영해|참고해|넣어줘|만들어줘|수정해줘)$|"
        r"^(줄테니|참고로|그리고|혹시|ai가|claude가|너가)",
        re.IGNORECASE
    )
    def clean(text):
        lines = [l for l in text.splitlines() if not instruction_re.search(l.strip())]
        return "\n".join(lines)

    combined = "\n\n".join(filter(None, [
        clean(direct_input), slack_data, customer_web_data, industry_news
    ]))

    response = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=8096,
        system=[
            {
                "type": "text",
                "text": SYSTEM_PROMPT,
                "cache_control": {"type": "ephemeral"},
            }
        ],
        messages=[{
            "role": "user",
            "content": GENERATION_PROMPT.format(combined_input=combined)
        }],
    )

    raw = response.content[0].text.strip()
    if raw.startswith("```"):
        raw = raw.split("```")[1]
        if raw.startswith("json"):
            raw = raw[4:]
    return json.loads(raw.strip())
